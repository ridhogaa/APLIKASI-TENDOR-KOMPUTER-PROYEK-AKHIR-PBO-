    private void btn_cpassActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_cpassActionPerformed
        // TODO add your handling code here:
        db.koneksiDatabase();
        try {
            Statement statement = db.getKoneksi().createStatement();
            ResultSet rs = statement.executeQuery("SELECT * FROM user WHERE username = '" + this.getUsername() + "'");
            rs.next();
            if (rs.getRow() == 1) {
                if (jPasswordField_pwlama_cp.getText().equals(rs.getString("password"))) {
                    if (jPasswordField_pwbaru_cp.getText().matches("[0-9a-zA-Z]+")
                            && jPasswordField_pwbaru_cp.getText().equals(jPasswordField_pwbaruconfirm_cp.getText())) {
                        try {
                            String query = "update user set password = ? where username = ? ";
                            PreparedStatement state = db.getKoneksi().prepareStatement(query);
                            state.setString(1, jPasswordField_pwbaru_cp.getText());
                            state.setString(2, getUsername());
                            state.execute();
                            JOptionPane.showMessageDialog(null, "Update Password Berhasil!");
                            new LoginAndRegisterForm().setVisible(true);
                            this.dispose();
                            clear();
                        } catch (Exception e) {

                        }
                    } else {
                        JOptionPane.showMessageDialog(null, "Password baru tidak sinkron atau memakai symbol!");
                        clear();
                    }
                } else {
                    JOptionPane.showMessageDialog(null, "Password lama tidak sesuai!");
                    clear();
                }
            }
        } catch (Exception e) {

        }

    }//GEN-LAST:event_btn_cpassActionPerformed

