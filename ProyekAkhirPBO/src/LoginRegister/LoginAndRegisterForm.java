    private void btn_updateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_updateActionPerformed
        db.koneksiDatabase();
        try {
            String query = "SELECT * FROM user WHERE email = '" + jTextField_emailforgot.getText() + "'";
            Statement st = db.getKoneksi().createStatement();
            ResultSet rsLogin = st.executeQuery(query);
            rsLogin.next();
            if (jTextField_emailforgot.getText().matches("[a-zA-Z0-9._]+@([a-zA-Z]{2,5})+(\\.[a-z]{2,5})+")) {
                if (rsLogin.getRow() == 1) {
                    if (jPasswordField_pwbaru.getText().matches("[0-9a-zA-Z]+")) {
                        if (jPasswordField_pwbaru.getText().equals(jPasswordField_pwbaruconfirm.getText())) {
                            String queryup = "update user set password = ? where email = ?";
                            PreparedStatement state = db.getKoneksi().prepareStatement(queryup);
                            state.setString(1, jPasswordField_pwbaru.getText());
                            state.setString(2, jTextField_emailforgot.getText());
                            state.execute();
                            JOptionPane.showMessageDialog(null, "Password berhasil diperbarui!");
                            clear();
                            panel_main.setVisible(true);
                            panel_lupaPass.setVisible(false);
                        } else {
                            JOptionPane.showMessageDialog(null, "Password tidak sinkron!");
                            clear();
                        }
                    }
                } else {
                    JOptionPane.showMessageDialog(null, "Email tidak tersedia!");
                    clear();
                }
            } else {
                JOptionPane.showMessageDialog(null, "Email tidak valid!");
                clear();
            }
        } catch (Exception e) {

        }
    }//GEN-LAST:event_btn_updateActionPerformed

